
	public class Score {

		private int teamOneScore;
		private int teamTwoScore;

		public Score(int teamOneScore, int teamTwoScore) {
			this.teamOneScore = teamOneScore;
			this.teamTwoScore = teamTwoScore;
		}

		public getTeamOneScore() {
			return teamOneScore;
		}

		public getTeamTwoScore() {
			return teamTwoScore;
		}
	}